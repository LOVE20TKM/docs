#!/usr/bin/env python3
from __future__ import annotations

import json
from collections import Counter, defaultdict
from pathlib import Path


def render_case(case: dict[str, object]) -> list[str]:
    secondary_skills = case["secondary_skills"]
    secondary_text = ", ".join(f"`{skill}`" for skill in secondary_skills) if secondary_skills else "_none_"

    lines = [
        f"### {case['title']}",
        "",
        f"- `id`: `{case['id']}`",
        f"- Secondary skills: {secondary_text}",
        f"- Expected output: {case['expected_output']}",
        "- Prompt:",
        "",
        f"> {case['prompt']}",
        "",
        "- Must cover:",
    ]
    lines.extend(f"- {item}" for item in case["must_cover"])
    lines.append("")
    return lines


def main() -> int:
    repo_root = Path(__file__).resolve().parents[2]
    source_path = repo_root / "ai" / "evals" / "love20_skill_benchmarks.json"
    output_path = repo_root / "ai" / "evals" / "love20_skill_benchmarks.md"

    payload = json.loads(source_path.read_text(encoding="utf-8"))
    benchmarks = payload["benchmarks"]

    grouped: dict[str, list[dict[str, object]]] = defaultdict(list)
    for benchmark in benchmarks:
        grouped[benchmark["primary_skill"]].append(benchmark)

    counts = Counter(benchmark["primary_skill"] for benchmark in benchmarks)

    lines = [
        "# LOVE20 Skill Benchmarks",
        "",
        "This file is generated by `ai/scripts/generate_skill_benchmark_catalog.py`.",
        "It is a human-readable view of the benchmark prompt source data in `ai/evals/love20_skill_benchmarks.json`.",
        "",
        "## Coverage Summary",
        "",
        "| Skill | Primary benchmark count |",
        "| --- | ---: |",
    ]
    for skill in sorted(counts):
        lines.append(f"| `{skill}` | {counts[skill]} |")

    for skill in sorted(grouped):
        lines.extend(["", f"## {skill}", ""])
        for case in grouped[skill]:
            lines.extend(render_case(case))

    output_path.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
